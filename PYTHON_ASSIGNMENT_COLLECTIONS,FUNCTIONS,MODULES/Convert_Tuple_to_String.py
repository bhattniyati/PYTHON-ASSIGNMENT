'''
    Write a Python program to convert a tuple to a string.
'''

# Define Tuple
t1= ('H','E','L','L','O',' ','W','O','R','L','D')

# using join method : convert a tuple to a string.
result_string= ''.join(t1)

# Display original tuple
print("Original Tuple: ",t1)

# Display Resulting String
print("Tuple to a String: ",result_string)
