'''
    Write a Python program to calculate the area of a trapezoid.
'''

# input a hieght of trapezoid
h= float(input("Hieght of Trapezoid: "))

# input a base one
base_one= float(input("Base one value: "))

# input a base two
base_two= float(input("Base two value: "))


# formulaa of area
area= ((base_one+base_two) /2) *h


# display the result
print("Area is: ",area)
